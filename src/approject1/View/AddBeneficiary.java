    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if(jTextField1.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Kindly enter the name");
        }
        else if(jTextField2.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Kindly enter the serial number");
        }
        else if(!isNumeric(jTextField2.getText()))
        {
            JOptionPane.showMessageDialog(this, "Kindly enter numeric value for the serial number");
        }

        else if(jTextField3.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Kindly enter the city");
        }
        else if(jTextField4.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Kindly enter the country");
        }
        else if(jTextField5.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Kindly enter the phone no.");
        }
        else if(jTextField6.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Kindly enter the email");
        }
        else if(jComboBox1.getSelectedItem().equals("Select Year")){
            JOptionPane.showMessageDialog(this, "Kindly enter the year");
        }
        else if(jComboBox2.getSelectedItem().equals("Select Month")){
            JOptionPane.showMessageDialog(this, "Kindly enter the month");
        }
        else if(jComboBox3.getSelectedItem().equals("Select Date")){
            JOptionPane.showMessageDialog(this, "Kindly enter the date");
        }
        else{
            String name,city,country,phoneNo,email;
            int bId = Integer.parseInt(jTextField2.getText());
            name = jTextField1.getText();
            city = jTextField3.getText();
            country = jTextField4.getText();
            phoneNo = jTextField5.getText();
            email = jTextField6.getText();
            String y = jComboBox1.getSelectedItem().toString();
            String m = jComboBox2.getSelectedItem().toString();
            String d = jComboBox3.getSelectedItem().toString();
            String totalDate = y+ "-" + m + "-" + d;
            java.sql.Date date = java.sql.Date.valueOf(totalDate);
            Beneficiary b = new Beneficiary(bId,name,date,city,country,phoneNo,email);
            org.InsertBeneficiary(b);

            new MainUIOrg(org).setVisible(true);
        }

    }//GEN-LAST:event_jButton1ActionPerformed

