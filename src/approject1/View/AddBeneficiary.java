    private void jComboBox2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboBox2ActionPerformed
        if(jComboBox2.getSelectedItem().equals("01") || jComboBox2.getSelectedItem().equals("03") || jComboBox2.getSelectedItem().equals("05") || jComboBox2.getSelectedItem().equals("08") || jComboBox2.getSelectedItem().equals("10")|| jComboBox2.getSelectedItem().equals("12"))
        {
            jComboBox3.removeAllItems();
            for(String str : d) {
                jComboBox3.addItem(str);
            }
        }
        else if(jComboBox2.getSelectedItem().equals("04")|| jComboBox2.getSelectedItem().equals("06")|| jComboBox2.getSelectedItem().equals("09")|| jComboBox2.getSelectedItem().equals("11"))
        {
            jComboBox3.removeAllItems();
            for(String str : d1) {
                jComboBox3.addItem(str);
            }

        }
        else if(jComboBox2.getSelectedItem().equals("02") && !(jComboBox1.getSelectedItem().equals("Select Year")))
        {
            boolean leap = false;
            int year = Integer.parseInt(jComboBox1.getSelectedItem().toString());
            if(year % 4 == 0)
            {
                if( year % 100 == 0)
                {
                    // year is divisible by 400, hence the year is a leap year
                    if ( year % 400 == 0)
                    leap = true;
                    else
                    leap = false;
                }
                else
                leap = true;
            }
            else
            leap = false;
            if(leap)
            {
                jComboBox3.removeAllItems();
                for(String str : d2) {
                    jComboBox3.addItem(str);
                }
            }
            else{
                jComboBox3.removeAllItems();
                for(String str : d3) {
                    jComboBox3.addItem(str);
                }

            }
        }
    }//GEN-LAST:event_jComboBox2ActionPerformed

